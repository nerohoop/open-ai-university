/**
 * Represents a course or resource in the directory.
 */
export interface Course {
  /** Unique identifier */
  id: string;
  /** When added to directory (ISO string) */
  created_at: string;
  /** Last updated (ISO string) */
  updated_at: string;
  /** URL to a cover image for the resource */
  image_url?: string | null;
  /** Headline/title of the content */
  title: string;
  /** Subtitle or secondary headline */
  subtitle?: string | null;
  /** Short summary */
  description: string;
  /** Resource type */
  type: CourseType;
  /** e.g., 'Coursera', 'GitHub', 'YouTube' */
  source_platform: string;
  /** Link to the original resource */
  source_url: string;
  /** Author or organization name */
  author?: string | null;
  /** Additional metadata */
  metadata?: Record<string, unknown>;
  /** Technology stack, topics, etc. */
  tags: string[];
  /** Is this a featured resource? */
  featured: boolean;
  /** Intended audience for the content */
  audience: string[];
  /** Estimated time to complete (e.g., "8 hours") */
  duration: string;
  /** Difficulty level */
  difficulty: Difficulty;
  /** List of main topics covered */
  topics: string[];
}

export enum CourseType {
  Course = "course",
  Repository = "repository",
  Event = "event",
  Tool = "tool",
}

export enum Difficulty {
  Beginner = "Beginner",
  Intermediate = "Intermediate",
  Advanced = "Advanced",
}

